package Modal.BEAN;

public class Account {
	private String user;
	private String pass;
	
	public String getUsername() {
		return user;
	}
	public String getPassword() {
		return pass;
	}
	public void setUsername(String username) {
		this.user = username;
	}
	public void setPassword(String password) {
		this.pass = password;
	}
}
